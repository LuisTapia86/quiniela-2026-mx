def get_lang() -> str:
    raw = (session.get("lang") or DEFAULT_LANG).strip().lower()
    if raw not in SUPPORTED_LANGS:
        return DEFAULT_LANG
    return raw


def t(key: str, **kwargs: Any) -> str:
    lang = get_lang()
    val = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS[DEFAULT_LANG].get(key) or key
    if kwargs:
        try:
            return val.format(**kwargs)
        except Exception:
            return val
    return val


def tr(key: str, **kwargs: Any) -> str:
    """Alias for Python route usage."""
    return t(key, **kwargs)
